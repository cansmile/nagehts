		<script>
			$(".tran").hide();
			var an = new Array();
			var an = ["kommt","kommen","komme","heiße","heißt","heißen","wohnst","wohne","wohnt"];
			$(document).ready(function() {
				$(".q").on("keyup", function () {
					var t = $(this);
					var q = $(this).val().length;
					var qn = ($(this).attr("id").substr(4))-1;
					if($(this).val() == an[qn].substr(0,q)) {
						$(this).removeClass("bg-danger");
						$(this).removeClass("bg-success");
						$(this).addClass("text-white text-weight-bold");
						$(this).addClass("bg-success");
					} else {
						$(this).removeClass("bg-danger");
						$(this).removeClass("bg-success");
						$(this).addClass("text-white text-weight-bold");
						$(this).addClass("bg-danger");
					}
					if(!$(this).val()) {
						$(this).removeClass("bg-danger");
						$(this).removeClass("bg-success");
						$(this).removeClass("text-white text-weight-bold");
					}

				})

				$(".q").on("focusout", function() {
					var t = $(this);
					var qn = ($(this).attr("id").substr(4))-1;
					a = an[qn];
					b = $(this).val();
					if(a == b) {
						$(this).addClass("bg-success");
						// 입력하면 똑같은 거 노랗게 만들기
						$(this).closest("table").find("span.py-2").each(function() {
							if($(this).text() == t.val()) {
								$(this).addClass("text-dark bg-warning");
							}
						})
						$(this).prop("disabled",true);
						$(this).addClass("text-weight-bold");
					} else {
						$(this).addClass("bg-danger");
					}

					if($(this).hasClass("bg-danger")) {
						ion.sound.play("Cartoon_Boing");
					} else if($(this).hasClass("bg-success")){
						ion.sound.play("Bama_Country_Country");
					}


					$(this).removeClass("text-white text-weight-bold");
					$(this).removeClass("bg-danger");
					$(this).removeClass("bg-success");

					if($("input:disabled").length == $(".q").length) {
						$("input:disabled").addClass("bg-success text-white");
						$(".tran").show();
					}


				})

				$("#qst-1").val(an[0]);
				$("#qst-4").val(an[3]);
				$("#qst-1").prop("disabled",true);
				$("#qst-4").prop("disabled",true);

				$("span.py-2").each(function () {
					if($(this).text() == an[0] || $(this).text() == an[3])	{
						$(this).addClass("text-dark bg-warning");
					}
				})

			});
			
		</script>

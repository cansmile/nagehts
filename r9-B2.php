    <script>
        $("#0").hide();
        $(".tran").hide();
        /* 각 문장 재생 횟수 초기화 */
        var hm = new Array();
        $(".so").each(function () {
            hm[$(this).attr("id")] = 0;
        });

        $(document).ready(function () {
            /* 소리 출력 전역 변수와 함수 */
            var sen = new Array(),
                pa = new Array(),
                he = new Array(),
                last;
            $(".so").each(function () {
                var t = $(this);
                var ti = t.attr("id");
                sen[ti] = 0;
                pa[ti] = t.html();
            });

            function stopAll() {
                $(".so").each(function () {
                    $(this).html(pa[$(this).attr("id")]);
                });
            }

            /* 문제 재생 */
            var nagehts = new Howl({
                src: ["./dev/sounds/Reihe 9/r9 B2.mp3"],
                sprite: {
                    "0": [750, 71105]
                },
                html5: true,
                volume: 1,
                format: "mp3",
                preload: true,
                onloaderror: function () {
                    $(".alert").append(
                        "<br /><strong class=\"font-weight-bold text-dark display-4\">페이지를 다시 읽어주시기 바래요.</strong>"
                    );
                    console.log("다시 읽어주세요!");
                },
                onload: function () {
                    /* 음성 준비되면 HV 버튼 나타내기 */
                    $("#0").show();
                    $(".alert").hide();
                    $(".so").on("click", function () {
                        var t = $(this);
                        var ti = t.attr("id");
                        if (($("div#last").text() == "" || t.text() == "❚❚") && !t.hasClass(
                                ".itm-lst")) {
                            $("#last").text(ti);
                            t.text("■");
                            nagehts.seek();
                            nagehts.play(ti);
                            sen[ti]++;
                            last = ti;
                            $("#cnt-" + ti).text(sen[ti]);
                        } else if (last == ti && nagehts.playing($("div#last").text())) {
                            $("#last").text("");
                            t.html(pa[ti]);
                            nagehts.pause();
                            sen[ti]--;
                            $("#cnt-" + ti).text(sen[ti]);
                        }
                    });

                    $("[data-toggle='popover']").popover({
                        delay: {
                            'hide': 1000
                        },
                        container: "body"
                    });
                    $(".pop").click(function () {
                        /* 가장 먼저 지문에 'an' 넣기 */
                        if (!$(this).siblings().hasClass("an")) {
                            $(this).addClass("an");
                            $(this).addClass("btn-warning");
                            $(this).parent().children().removeClass(
                                "btn-light");
                        };
                        /* 문제 풀이 정도 업데이트 */
                        var perc = Math.round(($(".an").length / $(".q").length) *
                            100);
                        $(".progress>.bar").attr("width", perc + "%;");
                    });

                    /* 팝업 내용 사라지기 */
                    $(".pop").popover().click(function () {
                        setTimeout(function () {
                                $(".pop").popover('hide');
                            },
                            500);
                    });

                    /* 정답확인 */
                    $("#chk").on("click", function () {
                        if ($(this).attr("id") == "chk") {
                            if ($(".an").length < $(".q").length) {
                                var na = "";
                                $(".q").each(function () {
                                    if (!$(this).find("div").hasClass("an")) {
                                        if (na != "") {
                                            na += ", ";
                                        }
                                        na += $(this).attr("id").substr(4);
                                    };
                                });
                                alert("모든 문제를 풀어주세요.");
                            } else {
                                $(".tran").show();
                                $(this).attr("id", "done");
                                $(".pop").each(function () {
                                    $(this).removeClass("btn-info");
                                    if ($(this).hasClass("o") && $(this).hasClass(
                                            "an")) {
                                        $(this).removeClass("btn-warning");
                                        $(this).addClass("btn-success");
                                    } else if ($(this).hasClass("o")) {
                                        $(this).addClass("btn-primary");
                                    } else if ($(this).hasClass("an")) {
                                        $(this).addClass("btn-warning");
                                    } else {
                                        $(this).addClass("btn-light");
                                    };
                                });

                                /* 정답 확인 div 상자 배경색 속성 없애기 */
                                $(this).removeClass("btn-light ");
                                var qa = $(".q").length; /* 전체 문항 수 */
                                var qr = $(".btn-success").length; /* 맞춘 항목 수 */
                                var pe = (qr / qa) * 100; /* 정답 비율 */
                                var tcl = "white"; /* 기본 문자색 */

                                /* 분류 기준은 100%, 80%, 60%, 40% */
                                if (pe > 99) {
                                    var st = "원어민이세요?";
                                    var cl = "lime";
                                    var tcl = "dark";
                                } else if (pe > 74) {
                                    var st = "어! 좀 하시는데요~^^";
                                    var cl = "success";
                                } else if (pe > 49) {
                                    var st = "쓰읍~ 다시 해 보실까요?";
                                    var cl = "primary";
                                } else {
                                    var st = "좀 더 분발해 주세요~";
                                    var cl = "danger";
                                }
                                $(this).addClass("btn-" + cl + " text-" + tcl);
                                $(this).html("<h4>" + qa + "문제 중 " + qr + "개를 맞히셨네요!<br>" +
                                    st + "</h4>");
                            };
                        }
                    });
                    var pan = new Array();
                    pan = [1];
                    var il = $(".q").length;
                    for (var p = 0; p < pan.length; p++) {
                        var pani = "#qst-" + pan[p];
                        $("#qst-" + pan[p] + ">div.o").addClass("an");
                        $("#qst-" + pan[p] + ">div.o").addClass("btn-warning");
                        $("#qst-" + pan[p] + ">div.o").removeClass("btn-light");
                    }
                },
                onend: function () {
                    $("div#last").text("");
                    stopAll();
                    $("#cnt-" + last).text(sen[last]);
                    if (last == 0) {
                        if (sen[last] == 2) {
                            $(".tran").show();
                            $(".so").each(function () {
                                pa[last] = $("#" + last).html();
                            });
                        }
                    } else if (sen[last] == 2) {
                        if ($("#" + last).hasClass("itm")) {
                            $("#" + last + ">.tran").show();
                        }
                        $("#" + last).closest("tr").find(".tran").show();
                        pa[last] = $("#" + last).html();
                    }
                }
            });
        });

